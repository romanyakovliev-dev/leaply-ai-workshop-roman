export function buildPriorContext(
  priorTranscripts: Partial<
    Record<StageId, { role: string; content: string }[]>
  >
): string {
  const lines: string[] = []
  for (const stageId of STAGE_ORDER) {
    const t = priorTranscripts[stageId]
    if (!t || t.length === 0) continue
    const persona = STAGES[stageId].displayName
    lines.push(`\n--- Транскрипт чату кандидата з ${persona} ---`)
    for (const msg of t) {
      const who = msg.role === "user" ? "Кандидат" : persona
      lines.push(`${who}: ${msg.content}`)
    }
  }
  if (lines.length === 0) return ""
  return (
    "\n\n=== ПОПЕРЕДНІ ЧАТИ ===\nОсь що кандидат вже сказав попереднім співрозмовникам. Чіпляйся за невідповідності, посилайся на конкретні слова, перевіряй чи поточні відповіді узгоджуються.\n" +
    lines.join("\n")
  )
}

export function buildSystemPrompt(
  stageId: StageId,
  turnsLeft: number,
  priorTranscripts: Partial<
    Record<StageId, { role: string; content: string }[]>
  >
): string {
  const base = STAGES[stageId].systemPrompt
  return base + TIME_AWARENESS(turnsLeft) + buildPriorContext(priorTranscripts)
}

